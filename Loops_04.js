// While Loop

//(runs the loop body till the condition given is true.)

let i = 0;
while (i <= 10) {
  console.log(i);
  i++;
}

//Do-while loop

//This first executes the code inside the loop body and then checks for the given condition.

let j = 2001;
do {
  console.log(j);
  j++;
} while (j < 10);

//For loop

// for (initializer; condition; iteration){
// loop body
// }

// Here, we can initialize the variable inside the loop.

for (let i = 0; i <= 10; i++) {
  console.log(i);
}
